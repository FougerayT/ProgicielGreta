package suivis;

public class suivi_du_jeune {
	protected int id;
	protected boolean notesFaibles;
	protected boolean absences;
	protected boolean stabilite;
	protected boolean RQTH;
	
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public boolean isNotesFaibles() {
		return notesFaibles;
	}
	public void setNotesFaibles(boolean notesFaibles) {
		this.notesFaibles = notesFaibles;
	}
	public boolean isAbsences() {
		return absences;
	}
	public void setAbsences(boolean absences) {
		this.absences = absences;
	}
	public boolean isStabilite() {
		return stabilite;
	}
	public void setStabilite(boolean stabilite) {
		this.stabilite = stabilite;
	}
	public boolean isRQTH() {
		return RQTH;
	}
	public void setRQTH(boolean rQTH) {
		RQTH = rQTH;
	}
	
	
	
	

}
